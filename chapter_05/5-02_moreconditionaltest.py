name = 'KELSEY'
print("\nTest using lower() method:")
print (name.lower() == 'kelsey')

print("\nNumerical test")
number = 18
print("Greater Than:")
print(number > 8)
print("Less Than:")
print(number < 21)
print("Greater Than or Equal To:")
print(number >= 18)
print("Less Than or Equal To:")
print(number <= 30)

print("\n'And' keyword test:")
print(number > 17 and number < 21)
print("\n'Or' keyword test:")
print(number < 1 or number > 30)

