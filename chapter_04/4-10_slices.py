#Create a list
squares = [value**2 for value in range(1, 15)]

#Print the first three items
print('The first three items in the list are:')
print(squares[0:3])
#Print the three items from the middle
print('Three items from the middle of the list are:')
print(squares[5:8])
#Print the last three items
print('The last three items from the list are:')
print(squares[-3:])
